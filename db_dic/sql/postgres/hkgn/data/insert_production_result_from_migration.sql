-- INSERT 완료 메시지
SELECT '생산 실적 ' || COUNT(*) || '건 INSERT 완료' AS result
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%';

-- 컬럼 매칭 현황
SELECT '컬럼 매칭 현황' AS section;

SELECT
    COUNT(*) AS total,
    COUNT(*) FILTER (WHERE material_cd IN (SELECT material_cd FROM hkgn.item_master)) AS material_cd_matched,
    COUNT(*) FILTER (WHERE plan_no IS NOT NULL) AS plan_no_matched,
    COUNT(*) FILTER (WHERE order_line_seq IS NOT NULL) AS order_line_seq_matched,
    COUNT(*) FILTER (WHERE work_request_no IS NOT NULL) AS work_request_no_filled
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%';

-- 날짜별 요약
SELECT production_date, COUNT(*) AS cnt,
       SUM(good_qty) AS total_good_qty,
       SUM(good_area) AS total_good_area,
       SUM(defect_qty) AS total_defect_qty,
       COUNT(*) FILTER (WHERE result_status = 'IN_PROGRESS') AS in_progress,
       COUNT(*) FILTER (WHERE result_status = 'CONFIRMED') AS confirmed
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%'
GROUP BY production_date
ORDER BY production_date;