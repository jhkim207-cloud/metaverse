-- ============================================
-- 2. UPDATE: material_cd (item_master.material_nm 매칭)
-- ============================================
-- item_master.material_nm과 정확히 일치하는 경우 material_cd 교정

UPDATE hkgn.production_result pr
SET material_cd = im.material_cd
FROM hkgn.item_master im
WHERE im.material_nm = pr.material_nm
  AND pr.production_result_no LIKE 'PR25-%';

SELECT 'material_cd UPDATE: ' || COUNT(*) || '건' AS result
FROM hkgn.production_result pr
JOIN hkgn.item_master im ON im.material_cd = pr.material_cd
WHERE pr.production_result_no LIKE 'PR25-%';

-- material_cd 매칭 실패 확인 (item_master에 없는 material_nm)
SELECT 'material_cd 미매칭' AS section, material_nm, COUNT(*) AS cnt
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%'
  AND material_cd NOT IN (SELECT material_cd FROM hkgn.item_master)
GROUP BY material_nm
ORDER BY cnt DESC;

-- ============================================
-- 3. UPDATE: plan_no (production_plan.plan_no 매칭)
-- ============================================
-- 엑셀 작지번호(order_no) = production_plan.plan_no

UPDATE hkgn.production_result pr
SET plan_no = pp.plan_no
FROM hkgn.production_plan pp
WHERE pp.plan_no = pr.order_no
  AND pr.production_result_no LIKE 'PR25-%';

SELECT 'plan_no UPDATE: ' || COUNT(*) || '건' AS result
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%'
  AND plan_no IS NOT NULL;

-- ============================================
-- 4. UPDATE: order_line_seq (엑셀 주문번호로 sales_order_detail 매칭)
-- ============================================
-- 엑셀 주문번호(order_no) + material_nm/material_cd로 sales_order_detail.line_seq 조회

-- 전략 1: order_no + material_nm 정확 매칭
UPDATE hkgn.production_result pr
SET order_line_seq = sd.line_seq
FROM hkgn.sales_order_detail sd
WHERE sd.order_no = pr.order_no
  AND sd.material_nm = pr.material_nm
  AND pr.production_result_no LIKE 'PR25-%'
  AND pr.order_line_seq IS NULL;

-- 전략 2: order_no + material_cd 매칭 (material_nm 불일치 대비)
UPDATE hkgn.production_result pr
SET order_line_seq = sd.line_seq
FROM hkgn.sales_order_detail sd
WHERE sd.order_no = pr.order_no
  AND sd.material_cd = pr.material_cd
  AND pr.production_result_no LIKE 'PR25-%'
  AND pr.order_line_seq IS NULL;

-- 전략 3: order_no + material_nm LIKE 매칭 (부분 일치)
UPDATE hkgn.production_result pr
SET order_line_seq = sd.line_seq
FROM hkgn.sales_order_detail sd
WHERE sd.order_no = pr.order_no
  AND (sd.material_nm LIKE '%' || pr.material_nm || '%'
       OR pr.material_nm LIKE '%' || sd.material_nm || '%')
  AND pr.production_result_no LIKE 'PR25-%'
  AND pr.order_line_seq IS NULL;

SELECT 'order_line_seq UPDATE: ' || COUNT(*) || '건' AS result
FROM hkgn.production_result
WHERE production_result_no LIKE 'PR25-%'
  AND order_line_seq IS NOT NULL;

-- 진단: order_line_seq 미매칭 확인
SELECT 'order_line_seq 미매칭' AS section, pr.order_no, pr.material_nm, COUNT(*) AS cnt
FROM hkgn.production_result pr
WHERE pr.production_result_no LIKE 'PR25-%'
  AND pr.order_line_seq IS NULL
GROUP BY pr.order_no, pr.material_nm
ORDER BY cnt DESC;

-- 진단: sales_order_detail에 해당 order_no 존재 여부
SELECT DISTINCT pr.order_no,
    CASE WHEN sd.order_no IS NOT NULL THEN 'O' ELSE 'X' END AS in_sales_order
FROM hkgn.production_result pr
LEFT JOIN hkgn.sales_order_detail sd ON sd.order_no = pr.order_no
WHERE pr.production_result_no LIKE 'PR25-%'
ORDER BY pr.order_no;

